        /// </summary>
        Json = 1,

        /// <summary>
        /// Represents the XML type
        /// </summary>
        Xml = 2,

        /// <summary>
        /// Represents the HTML type
        /// </summary>
        Html = 3,

        /// <summary>
        /// Represents the None type
        /// </summary>
        None = 4,
    }
}
